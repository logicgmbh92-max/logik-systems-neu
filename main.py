@app.post("/v1/audit")
async def start_compliance_audit(request: ComplianceAuditRequest):
    """
    Main endpoint: Start a compliance assessment
    
    Returns:
    - Audit ID for tracking
    - Preliminary assessment
    - Disclaimer
    - Next steps
    """
    
    # Validate tier
    if request.tier not in TIERS:
        raise HTTPException(status_code=400, detail=f"Invalid tier. Choose from: {list(TIERS.keys())}")
    
    # Generate audit ID
    audit_id = generate_audit_id()
    
    # Generate assessment
    assessment = await generate_compliance_assessment(request)
    
    if assessment["status"] != "success":
        raise HTTPException(status_code=500, detail="Assessment generation failed")
    
    # Create audit record in Supabase
    audit_record = {
        "audit_id": audit_id,
        "system_name": request.system_name,
        "tier": request.tier,
        "status": "completed",
        "assessment": assessment["analysis"],
        "disclaimer": get_compliance_disclaimer(),
        "created_at": datetime.utcnow().isoformat()
    }
    
    try:
        supabase.table("audits").insert(audit_record).execute()
    except Exception as e:
        print(f"Database error: {str(e)}")
        # Continue even if DB fails - return assessment
    
    return {
        "audit_id": audit_id,
        "tier": request.tier,
        "service_tier_info": TIERS[request.tier],
        "assessment": assessment["analysis"],
        "disclaimer": get_compliance_disclaimer(),
        "next_steps": [
            "1. Review this automated assessment with your compliance team",
            "2. Engage qualified professionals for detailed legal review",
            "3. Implement recommended changes",
            "4. Schedule follow-up assessment"
        ],
        "support_contact": "support@logik-systems.com"
    }

