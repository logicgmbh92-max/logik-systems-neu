@app.post("/webhook")
async def stripe_webhook(request: Request, sig_header: str = Header(None)):
    """
    Stripe webhook handler for payment events
    """
    payload = await request.body()
    
    try:
        event = stripe.Webhook.construct_event(
            payload,
            sig_header,
            STRIPE_WEBHOOK_SECRET
        )
    except ValueError:
        raise HTTPException(status_code=400, detail="Invalid payload")
    except stripe.error.SignatureVerificationError:
        raise HTTPException(status_code=400, detail="Invalid signature")
    
    # Handle payment success
    if event['type'] == 'invoice.payment_succeeded':
        invoice = event['data']['object']
        print(f"[✓] Payment received: {invoice['id']} - Amount: {invoice['amount_paid']/100} EUR")
        
        # Log to database
        try:
            supabase.table("payments").insert({
                "stripe_invoice_id": invoice['id'],
                "amount_eur": invoice['amount_paid'] / 100,
                "status": "completed",
                "created_at": datetime.utcnow().isoformat()
            }).execute()
        except Exception as e:
            print(f"Payment logging error: {str(e)}")
    
    return {"status": "success"}

