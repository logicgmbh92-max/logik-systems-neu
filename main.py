# --- 5. UTILITY FUNCTIONS ---

def generate_audit_id():
    """Generate unique audit identifier"""
    timestamp = int(time.time() * 1000)
    random_hash = hashlib.sha256(str(timestamp).encode()).hexdigest()[:8]
    return f"AUDIT-{timestamp}-{random_hash.upper()}"

def get_compliance_disclaimer():
    """Standard disclaimer for all reports"""
    return (
        "⚠️ IMPORTANT DISCLAIMER:\n"
        "This report is generated by an AI-assisted analysis system and is NOT a substitute for professional legal or compliance advice.\n"
        "Logik Systems EOOD is a technology provider, not a licensed compliance consultant or law firm.\n"
        "All findings should be reviewed by qualified compliance professionals before implementation.\n"
        "This assessment is based on the information provided and current EU AI Act guidelines as of the analysis date.\n"
        "Logik Systems EOOD assumes no liability for decisions made based solely on this automated analysis."
    )

async def generate_compliance_assessment(request: ComplianceAuditRequest) -> dict:
    """
    Generate AI-assisted compliance assessment
    Uses GPT-4 to analyze against EU AI Act requirements
    """
    prompt = f"""
    You are a compliance assessment assistant for EU AI Act compliance.
    
    IMPORTANT: Your analysis is AI-generated and must be reviewed by qualified professionals.
    
    Analyze the following AI system for EU AI Act compliance:
    
    System Name: {request.system_name}
    Description: {request.system_description}
    Model Type: {request.ai_model_type}
    Use Case: {request.use_case}
    Data Categories: {', '.join(request.data_categories)}
    
    Provide:
    1. Risk Level Assessment (LOW/MEDIUM/HIGH)
    2. Compliance Score (0-100)
    3. Key Findings (list 5-7 specific areas)
    4. Recommendations (list 5-7 actionable steps)
    
    Format as JSON with keys: risk_level, compliance_score, findings, recommendations
    
    Remember: This is an automated analysis tool, not legal advice.
    """
    
    try:
        response = await client.chat.completions.create(
            model="gpt-4-turbo",
            messages=[
                {
                    "role": "system",
                    "content": "You are a compliance assessment tool. Provide structured, honest analysis. Always remind users this is AI-generated and not legal advice."
                },
                {
                    "role": "user",
                    "content": prompt
                }
            ],
            temperature=0.7,
            max_tokens=1500
        )
        
        return {
            "status": "success",
            "analysis": response.choices[0].message.content
        }
    except Exception as e:
        return {
            "status": "error",
            "message": str(e)
        }

